
public class BSwitch {

	public static void main(String[] args) {
		
		String find= "no";
		switch (find) {
		case "yes":
			
			System.out.println(" Lets celebrate!!!");
			break;
			
		case "no":
			System.out.println("Keep try!!!");
			break;
		default:
			System.out.println("Don't get upset!!!");
			
			break;
		}

	}

}
